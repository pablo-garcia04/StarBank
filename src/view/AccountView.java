    private void crearButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crearButtonActionPerformed

        int error = validator.validateInputAccountView(id.getText(), savings.isSelected(), currents.isSelected(),
                idClient.getText(), np.isSelected(), cp.isSelected(), errorAccountID, errorAccountType, errorClientID,
                errorClientType);
        if (error != 0) {
            return;
        }
        if (!validator.isDuplicateClient(idClient.getText(), savings.isSelected())) {
            getClientView();
        } else {
            cashier.addAccount(id.getText(), idClient.getText(), savings.isSelected());
            Message.getMessageSuccefullAccount();
            this.dispose();
            cashier.setVisible(true);
        }
    }//GEN-LAST:event_crearButtonActionPerformed

