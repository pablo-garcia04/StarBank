    public void getClientView() {
        if (JOptionPane.showConfirmDialog(null, "La identificacion de cliente " + idClient.getText() + " no se encuentra registrada ¿Desea registarlo?", "Identificacion no registrada",
                JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            ClientView abrir = new ClientView(cashier, this);
            abrir.setVisible(true);
            this.setVisible(false);
            if (np.isSelected()) {
                abrir.personActivate(idClient.getText());
            } else {
                abrir.companyActivate(idClient.getText());
            }
        }
    }

